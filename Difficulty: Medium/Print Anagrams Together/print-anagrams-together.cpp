class Solution{
  public:
    vector<vector<string> > Anagrams(vector<string>& v) {
        //code here
        vector<vector<string>> ans;
        string temp;
        map<string , vector<string>> mpp;
        for(int i=0;i<v.size();i++){
            temp = v[i];
            std::sort(temp.begin(),temp.end());
            mpp[temp].push_back(v[i]);
        }
        for(auto i : mpp){
            ans.push_back(i.second);
        }
        return ans;
    }
};

